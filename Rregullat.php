    <h3>RREGULLAT</h3>
    <P><b>Udhëzim</b></P>

    <P>Nr.21, datë 30.7.2009</P>

 

    <P>Për procedurat e pranimit dhe të regjistrimit në programet e studimit të doktoratës, në institucionet publike të arsimit të lartë</P>

 

    <P>Në mbështetje të nenit 102 të Kushtetutës, të nenit 26, pika 1.3, e vijues të ligjit nr.9741, datë 21.5.2007 “Për arsimin e lartë në Republikën e Shqipërisë”, të ndryshuar, të vendimit nr.864, datë 5.12.2007 të Këshillit të Ministrave “Për hapjen e programeve të studimit të doktoratës në institucionet publike të arsimit të lartë dhe përcaktimin e kushteve, që duhet të plotësojë studenti për marrjen e diplomës për gradën shkencore “Doktor”, të ndryshuar</P>

 

    <P>Udhëzoj:</P>

 
<dl>
<dt>1. Pranimin e kandidatëve në programet e studimit të doktoratës duke iu referuar numrit të kuotave të dhëna për programin përkatës.</dt>
<dt>2. Për t’u pranuar në një program studimi të doktoratës, kandidati duhet të zotërojë një diplomë të nivelit të dytë, DND, një diplomë të integruar të nivelit të dytë, DIND, dhe çdo diplomë tjetër të vlerësuar të njëvlefshme me to, në fushën e programit të doktoratës apo në një fushë të përafërt të përcaktuar më parë në rregulloren e programit përkatës të doktoratës.</dt>
<dt>3. Gjithashtu kanë të drejtë të pranohen në programe të doktoratës edhe ata kandidatë që zotërojnë një diplomë apo certifikatë të kualifikimeve të ciklit të tretë të studimeve.</dt>
<dt>4. Njësitë kryesore të institucioneve publike të arsimit të lartë përcaktojnë kriteret e pranimit të kandidatëve, pasi marrin paraprakisht mendimin e këshillit të profesorëve dhe i bëjnë ato publike paraprakisht.</dt>
<dt>5. Njësitë kryesore të institucioneve të arsimit të lartë që janë përgjegjëse për organizimin e programit të studimit të doktoratës organizojnë edhe procesin e pranimit dhe regjistrimit të kandidatëve në çdo program përkatës studimi doktorate. Procesi monitorohet nga rektorati dhe Ministria e Arsimit dhe Shkencës.</dt>
<dt>6. Me caktimin e kuotave nga Këshilli i Ministrave për një program studimi doktorate, institucioni i arsimit të lartë përgjegjës shpall menjëherë hapjen e procedurës së pranimit të kërkesave për një periudhë 2-javore pasi merr paraprakisht mendimin e njësisë kryesore përgjegjëse për organizimin e këtij programi studimi.</dt>
<dt>7. Aplikimi për regjistrim në programet e doktoratës bëhet pranë sekretarive të njësive kryesore përgjegjëse për organizimin e programit përkatës së doktoratës.</dt>
<dt>8. Kandidatët aplikojnë për t’u regjistruar në një program studimi doktorate me dosje individuale, e cila duhet të përmbajë dokumentet e mëposhtme:</dt>
<dd>a) Kërkesa e kandidatit drejtuar këshillit të profesorëve të njësisë kryesore përgjegjëse për organizimin dhe administrimin e doktoratës.</dd>
<dd>b) CV personale dhe bibliografi të punimeve shkencore, të cilat duhet të jenë të dokumentuara, nëse kandidati ka të tilla.</dd>
<dd>c) Kopje të noterizuar të diplomës së nivelit të dytë (DND) apo të diplomës së integruar të nivelit të dytë, apo të një diplome tjetër studimi, ekuivalente me to, të shoqëruar me listën e notave apo suplementin e diplomës. Diploma e huaj shoqërohet me vërtetimin e njohjes nga MASH-i.</dd>
<dd>d) Kopje të noterizuar të diplomave apo certifikatave të kualifikimeve të ciklit të tretë apo të një diplome ose certifikate ekuivalente me to, shoqëruar me listën e notave apo suplementin e diplomës, nëse kandiati disponon një të tillë.</dd>
<dd>e) Vërtetim për vazhdimin e programeve të studimit të specializimit, së bashku me listën e notave të fituara deri në atë moment, nëse kandidati është duke ndjekur një program të tillë.</dd>
<dd>f) Fotokopje të noterizuar të kartës së indentitetit/pasaportës.</dd>
<dd>g) Dy fotografi.</dd>
<dd>h) Dy rekomandime nga persona me titull akademik.</dd>
<dd>i) Një deklaratë që pranon të shlyejë të gjitha detyrimet financiare lidhur me koston e këtyre studimeve, sipas përcaktimeve të bëra nga vetë institucioni.</dd>
<dd>j) Dëshmi të gjuhës/gjuhëve të huaja (fotokopje të noterizuar).</dd>
<dd>k) Vërtetim nga qendra e punës, nëse është i punësuar.</dd>
<dd>l) Mandat për tarifën e aplikimit për konkurrim prej 2000 lekë.</dd>
<dt>9. Përzgjedhja e kandidatëve bëhet nga këshilli i profesorëve të njësisë kryesore të institucioneve të arsimit të lartë përgjegjëse për organizimin e programit përkatës të doktoratës nëpërmjet një konkurrimi me dosje, ku kandidatët nga personeli akademik të punësuar me kohë të plotë në institucionet e arsimit të lartë kanë përparësi.</dt>

<dt>10. Këshilli i profesorëve të njësisë kryesore përgjegjëse për programin e doktoratës ngre komisione ad-hoc me anëtarë të tij sipas fushës përkatëse të çdo programi doktorate për përzgjedhjen e kandidatëve fitues në përputhje me kriteret e pranimit të vendosura dhe të shpallura publikisht më parë. Kryetari i çdo komsioni të tillë është dekani.</dt>

<dt>11. Një javë pas mbylljes së aplikimeve komisionet ad-hoc paraqesin listat e kandidatëve fitues në këshillin e profesorëve të njësisë kryesore. Listat e kandidatëve fitues miratohen nga këshilli i profesorëve dhe shpallen nga dekani jo më vonë se 2 javë nga mbyllja e aplikimeve.</dt>

<dt>12. Dekani i njësisë kryesore përcakton afatet e ankimimit të kandidatëve të shpallur jo fitues. Këshilli i profesorëve shqyrton çdo ankimim dhe shprehet me vendim. Vendimi i tij është përfundimtar.</dt>

<dt>13. Kandidatët fitues regjistrohen në sekretarinë e njësisë kryesore përgjegjëse për programin përkatës të doktoratës për një periudhë njëjavore të përcaktuar nga dekani i njësisë kryesore.</dt>

<dt>14. Kandidatët fitues duhet të paguajnë një tarifë 5000 lekë për regjistrim në programin e studimit të doktoratës ku janë shpallur fitues. Dosja individuale me dokumentet e aplikantëve jofitues u kthehet atyre kundrejt kërkesës në dorë ose me postë.</dt>

<dt>15. Studentët e regjistruar, të cilët kanë përfunduar ose vijojnë programe studimi të ciklit të tretë, nuk janë të detyruar të grumbullojnë 60 kreditet e studimeve të organizuara teorike, pjesë e programit të studimit të doktoraturës, vetëm nëse programi i studimit të cilin ata kanë përfunduar apo vijojnë ka formim teorik të njëjtë ose të afërt me atë të programit të doktoratës.</dt>

Këshilli i profesorëve ngre komisionet e drejtuara nga kordinatori i programit të doktoratës për njohjen dhe transferimin e krediteve të fituara nga studentët e regjistruar, të cilët kanë përfunduar ose vijojnë një program studimi të ciklit të tretë në varësi të përputhshmërisë së tyre me programin e studimit të doktoratës ku ai është regjistruar. Për çdo student të tillë përcaktohen veprimtaritë formuese, modulet lëndore dhe kreditet përkatëse, të cilat ai është i detyruar t’i fitojë nga plani i studimeve të organizuara të programit të studimit përkatës të doktoratës.</dt>

<dt>16. Tarifa e shkollimit për një program doktorate është e përcaktuar me vendim të Këshillit të Ministrave, ku brenda vlerës së përcaktuar paraqet:</dt>

a)vlerën për pjesën e studimeve të organizuara, e cila ndahet për çdo modul lëndor në varësi të krediteve që i takojnë atij;
b) vlerën për pjesën e studimeve për periudhën që përmbledh kërkimin shkencor dhe përgatitjen e disertacionit deri në marrjen e diplomës për gradën shkencore “Doktor”.
<dt>17. Tarifa e shkollimit paguhet nga studenti në këste të përcaktuara nga njësia kryesore e institucionit të arsimit të lartë përgjegjës për organizimin e doktoratës. Kësti i parë paguhet menjëherë me regjistrimin e studentit dhe është sa vlera e tarifës për pjesën e studimeve të organizuara. Studentët që kanë transferuar kredite nga programe të tjera të ciklit të tretë paguajnë vlerën përkatëse sipas moduleve lëndore dhe krediteve që ata duhet të fitojnë në programin përkatës të doktoratës.</dt>

<dt>18. Ngarkohet Drejtoria e Politikave të Arsimit të Lartë dhe institucionet e arsimit të lartë të cilat organizojnë studime të doktoratës për zbatimin e këtij udhëzimi.</dt>
</dl>
<p>Ky udhëzim hyn në fuqi menjëherë dhe botohet në Fletoren Zyrtare.</p>

 

