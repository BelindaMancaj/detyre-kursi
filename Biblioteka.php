<p><b>Bibloteka Shkencore e UPT.</b></p>
<p>Me vendim Nr. 11, datë 01.06.1995, të Ministrisë së Arsimit dhe Shkencës, Biblioteka e Universitetit Politeknik, është riemëruar Biblioteka Shkencore e Universitetit Politeknik.</p>
<p>Biblioteka Shkencore është pjesë e strukturës dhe në vartësi direkte të Rektoratit të Universitetit Politeknik. Biblioteka, me aktivitetin e saj, i shërben proçesit mësimor, punës kërkimore shkencore dhe u ofron literaturë stafit akademik, punonjësve shkencorë dhe specialistëve të fushave të ndryshme të prodhimit, si dhe studentëve të Universitetit Politeknik.</p>
<p>Biblioteka Shkencore e kryen aktivitetin e saj në përputhje me rregulloren, e cila miratohet nga Rektorati i Universitetit Politeknik.</p>
<p><b>Katalogu elektronik:</b><br>
Për herë të parë në historinë e Bibliotekës Shkencore të Universitetit Politeknik, që prej vitit 2008 po krijohet Skedari Elektronik, duke përdorur Programin ProCite 4.0. 
<p><b>Shërbimet:</b></p>

<b><u>Orari i shërbimit për përdoruesit:</u></b><br>
<p>&#x204E; Biblioteka është e hapur nga e Hëna në të Premte nga ora 8°° - 19°°. Ditën e Shtunë nga ora 8.30- 14°°.</p>


<p><b>Shërbimet referencë që ofron biblioteka janë:</b></p>

&#x204E; Shërbim të drejtpërdrejtë: informacioni i plotë që u jepet përdoruesve të saj, për çdo problematikë që kërkohet.


&#x204E; Shërbime indirekte: që përfshin krijimin e katalogëve, bibliografive dhe të gjitha referencave ndihmëse, të cilat ndihmojnë përqasjen e përdoruesve për këto koleksione.
</p>
<p>
<b>Biblioteka ofron këto shërbime:</b>

 

<p>&#x204E; Përdorimi i materialeve të bibliotekës kryhet me huazim, si dhe në sallat e leximit, kur materialet nuk huazohen.</p>

 

 

<p>&#x204E; Shërbimi i koleksioneve të librave, të periodikut, të literatures referuese (enciklopedi, fjalorë, libra unikë, dizertacione) dhe qasje me literaturë në format elektronik.</p>

 

Mbështetur në LIGJIN Nr.8576, datë 3.2.2000 Për Bibliotekat në Republikën e Shqipërisë, kreu II “Të drejtat dhe përgjegjësitë e shtetasve për përdorimin e shërbimeve bibliotekare”
</p>
<p>
<b>Përdoruesi i bibliotekës ka të drejtë:</b><br></p>

 <ol>

 <li>të marrë informacion për koleksionet e bibliotekës dhe për koleksionet e bibliotekave të tjera nëpërmjet mjeteve tradicionale dhe elektronike të informacionit bibliotekar;</li>
 

 <li>të ndihmohet për kërkim dhe zgjedhje të botimeve informative;</li>
 

 <li>të shfrytëzojë koleksionet e bibliotekës në mjediset e saj, të fotokopjojë dhe të huazojë materiale bibliotekare, sipas rregullores së bibliotekës;</li>
 

 <li>të huazojë materiale ose fotokopje të tyre nga bibliotekat e tjera, brenda dhe jashtë vendit, nëpërmjet shërbimit ndërbibliotekar.</li>
 

 <li> e drejta për shërbim dhe informacion bibliotekar sigurohet falas dhe në mënyrë të barabartë për të gjithë përdoruesit e bibliotekave shtetërore.</li>
 

 <li>përdoruesit detyrohen të respektojnë rregulloren e bibliotekës.</li>
 
</ol><br>

