    <h3>PROGRAMI I STUDIMIT TIK</h3>
   <p><b> Viti akademik: 2009-2010 e në vazhdim</b></p>

<p>1. Emërtimi i përgjithshëm i programit të studimit të doktoratës</p>

<p><u>Në shqip</u></p>

<span class="tik">TEKNOLOGJITË E INFORMACIONIT DHE TË KOMUNIKIMIT (TIK)</span>

<p><u>Në anglisht</u></p>

<span class="tik">INFORMATION AND COMMUNICATION TECHNOLOGY (ICT)</span>

 

<p><b>2. Objektivat formues të programit të studimit të doktoratës</b></p>

<p>Programi i studimit të Doktoratës ka për qëllim të pajisë studentët me aftësitë dhe njohuritë e nevojshme në mënyrë të tillë që ata mund të kryejnë punë kërkimore të kualifikuar në universitete, trupa publike dhe organizata private; duke zhvilluar më tej progresin shkencor dhe teknologjik; si dhe do të krijojë një klasë udhëheqëse me nivel të lartë profesional.</p>

<p>Qëllimi i studimeve të thelluara në ciklin e tretë është të thellojë njohuritë bazë formuese të inxhinierëve, duke synuar përgatitjen e drejtpërdrejtë nga ana shkencore për të lejuar kualifikimin e mëtejshëm në fushat e inxhinierisë informatike, inxhinierisë elektronike, si dhe të inxhinierisë së telekomunikacioneve.</p>

<p> Puna studimore dhe projektuese në ciklin e tretë synon që studenti i doktoratës në mënyrë individuale të pasurojë njohuritë kërkimore shkencore të drejtimit të tezës së doktoratës, duke thelluar përgatitjen teoriko-shkencore për të lejuar zhvillimin e kapaciteteve dhe thellimin e njohurive në fushën komplekse inxhinierike që janë në një zhvillim teknologjik të vazhdueshëm.</p>

<p>Qëllimi i punës studimore dhe projektuese gjatë vitit të tretë është që studenti i doktoratës në mënyrë individuale të jetë i aftë të përfundojë punën kërkimore shkencore të tezës së doktoratës, duke arritur synimin për të përfunduar tezën e doktoratës, që i jep të drejtën për të kërkuar gradën “Doktor”, pasi ka zhvilluar kapacitetet laboratorike, ka thelluar njohuritë në programin përkatës  inxhinierik që ka zgjedhur, si dhe ka dhënë ide të reja në një zhvillim teknologjik të caktuar.</p>

<p> Programi i studimit të Doktoratës lejon të ushtrohet kërkimi shkencor në sektorët e projektimit inxhinierik, zhvillimit dhe menaxhimit të sistemeve të përpunimit të pajisjeve informatike dhe sistemeve të informacionit; planifikimin dhe menaxhimin e infrastrukturave dhe proceseve në sektorin e telekomunikacioneve; futjen e teknologjive të reja në sektorin e Teknologjisë së Informacionit. </p>

 

<p><b>3. Elementë të përgjithshëm të programit të studimit të doktoratës</b></p>

 

<span>Viti I  i studimeve në ciklin e tretë (formimi i thelluar teorik 60 kredite) </span>

<p>Qëllimi i studimeve të thelluara në vitin e parë të ciklit të tretë është të thellojë njohuritë bazë formuese të inxhinierëve, duke synuar përgatitjen e drejtpërdrejtë nga ana shkencore për të lejuar kualifikimin e mëtejshëm në fushat e inxhinierisë informatike, inxhinierisë elektronike, si dhe të inxhinierisë së telekomunikacioneve.</p>

       Viti i parë i studimeve të shkollës së doktoratës ka në total 60 kredite.<br>

       Semestri i parë (30 kredite) ka të njëjtat lëndë në të dy drejtimet e programit të studimit që ofron FTI. Lëndët kryesore që do të zhvillohen janë:
<dl>
<dd>- Arkitektura e Sistemeve të Përpunimit;</dd>

<dd>- Rrjetat e Avancuara të Kompjuterave;</dd>

<dd>- Shkrimi shkencor;</dd>

<dd>- Menaxhimi i projekteve.</dd>
</dl>
  <p><b>Semestri i dytë</b> (30 kredite) ofron lëndë me zgjedhje, sipas drejtimit të zgjedhur, si dhe sipas specifikës së tezës së doktoratës, për secilin kandidat të regjistruar.</p>
<dl>
<span>Drejtimi Inxhinieri Informatike:</span>

<dd>- Gjuhët dhe kompilatorët;</dd>

<dd>- Bazat e të dhënave dhe teknologjitë e tyre;</dd>

<dd>- Inteligjenca Artificiale, Agjentët dhe Rrjetat Neurale;</dd>

<dd>- Projektimi i Sistemeve operative;</dd>

<dd>- Inxhinieria e avancuar e software-it.</dd>
</dl>
<dl>
<span>Drejtimi Telekomunikacion dhe Inxhinieri Informacioni:</span>

<dd>- Teknologjitë e avancuara të Sistemeve të Telekomunikacioneve;</dd>        

<dd>- Sistemet e Komunikimit Mobile;</dd>    

<dd>- Rrjetat e avancuara të Telekomunikacioneve;</dd> 

<dd>- Përpunimi numerik i avancuar i sinjaleve;</dd> 

<dd>- Teoria e Informacionit dhe kodet;</dd> 

<dd>- Sensorët dhe rrjetat wireless.</dd> 

<p>Lëndët në vitin e parë ofrojnë formimin e thelluar shkencor të kërkuesit në drejtimet e teknologjive të informacionit. Titullarët e lëndëve të vitit të parë janë pjesë e stafit akademik efektiv me titull “Akademik” dhe/ose “Profesor”.</p>

 

<p><span>Viti II i studimeve në ciklin e tretë (kërkim/krijim 60 kredite)</span></p>

 

<p>Krahas përgatitjes teorike pas përfundimit të provimeve studentët e doktoratës duhet të ndjekin edhe plotësimin e kërkesave të mëposhtme:</p>

 

<p>Njohja me  kuadrin ligjor në fuqi: ligjin nr. 9741, dt. 21.05.2007 “Për arsimin e lartë në Republikën e Shqipërisë”  (i ndryshuar), VKM nr. 864 dt. 5.12.2007 “Për hapjen e programeve të studimeve të doktoratës në institucionet publike të arsimit të lartë dhe për përcaktimin e kushteve që duhet të plotësojë studenti për marrjen e diplomës për gradën shkencore “Doktor” (i ndryshuar), Udhëzimin nr. 5, dt. 22.01.2008 të MASH.</p>

<p>Njohja me udhëheqësin shkencor, me drejtimin kërkimor, me ecurinë brenda vendit, eksperiencën, arritjet etj. Kjo bëhet duke parë doktoratat, monografitë ose periodikët e botuar në aktivitete të ndryshme brenda, ose jashtë vendit. Këto mblidhen dhe me të gjitha formohet lista e bibliografisë pranë laboratorit ku do të kryhet studimi. Bëhen gjithë vizitat në institucionet ose laboratorët e ngjashëm brenda ose jashtë vendit, bëhet njohja me personat e interesuar në fushën studimore përkatëse dhe vendosen lidhjet midis tyre; bëhet njohja me arritjet e tyre dhe programet studimore në proces.</p>

<p>Njohja me arritjet në rajon dhe më gjerë lidhur me studimin e zgjedhur, si dhe vendosen lidhjet e mundshme me persona të interesuar. Kjo gjë fillohet nga informacioni i vlefshëm në internet, nga publikimet e huaja në Departament, në bibliotekat e vendit e deri te kontaktet me persona të njohur në institutet e ngjashme ose drejtpërdrejt me autorët e botimeve.</p>

<p>Njohja me programet dhe sistemet hardware që nevojen, duke krijuar mundësinë e blerjes dhe realizimit të sistemit për përpunimin e të dhënave.</p>

<p>Hartohet plani i punës i ndarë në vite, dhe paraqitet në një seminar të posaçëm të organizuar në Departament, me anëtarët e grupit kërkimor përkatës, duke ftuar dhe ekspertë të tjerë të fushës. </p>

<p>Në planin e punës duhen pasur parasysh ecuria e referimeve dhe publikimeve që kandidati duhet të realizojë për të qenë në përputhje me aktet ligjore në fuqi. Në këtë mënyrë, duhet të parashikohen të paktën një referim ose prezantim shkencor, nga të cilët një referim ose paraqitje në një veprimtari shkencore ndërkombëtare (simpozium, konferencë, kongres), i pranuar në bazë të një vlerësimi paraprak shkencor dhe i botuar në “proceedings” me kod ISBN-je. Gjithashtu, të parashikohet përgatitja për botim si autor i parë, i të paktën një artikulli shkencornë revista shkencore, të miratuara nga MASH.</p>

<p>Studimi, projektimi dhe zhvillimi i problemeve kryesore të tezës së doktoratës zhvillohen në vitin e dytë të shkollës së doktoratës, që konvertohen në total në masën prej 60 kredite.</p>

 

<p><span>Viti III i studimeve në ciklin e tretë (kërkim/krijim, botim artikuj dhe referime 60 kredite)<span></p>

 

<p>Vazhdon pjesa eksperimentale e studimit e vitit të dytë, gjithnjë duke ndjekur detyrat e caktuara në planin e punës:</p>
<dl>
  <dd> > Bashkëpunimi me institute të tjera, për të përftuar eksperiencë në laboratore kërkimore me emër në fushën përkatëse. Kjo duke shfrytëzuar lidhjet e vendosura jashtë vendit, kryesisht me universitetet ku kemi stafin e ftuar, si Gjermania, Franca, Italia, Japonia, SHBA, etj.. Do të kërkohet mbështetje financiare me projekte të përbashkëta me universitetet e BE, në kuadër të FP7; të bashkëpunimit me DAAD, si dhe ndonjë mundësi tjetër për të vizituar laboratoret më me eksperiencë, jashtë vendit, në kuadër të fondit të ekselencës, të projekteve të kërkim zhvillimit të MASH, etj..</dd>

  <dd> > Përpunimi i të dhënave, grupimi i tyre dhe nxjerrja e përgjithësimeve dhe e veçantive që paraqesin interes. Gjatë vitit të tretë organizohet pjesëmarrja në një veprimtari shkencore/profesionale ndërkombëtare (simpozium, konferencë, kongres), i pranuar në bazë të një vlerësimi paraprak shkencor dhe i botuar në “proceedings” me kod ISBN-je.</dd>

  <dd> > Përgatitja për botim si autor i parë, i të paktën dy artikuj shkencorënë revista shkencore, ku të paktën njëri prej tyre duhet të botohet ose pranohet për botim në një revistë shkencore ndërkombëtare, me bord editorial.<dd>

  <dd> > Hartimi i tezës së doktoratës bëhet në përputhje me “Rregullat për paraqitjen e punimit të disertacionit” të Rregullores së programit të studimit të doktoratës, miratuar nga Këshilli i Profesorëve të FTI, me Vendim nr. 6, datë 19.01.2009.</dd>
</dl>
 

