<p>
Fakulteti i Teknologjisë së Informacionit është krijuar me vendim të qeverisë në dhjetor 2007. Fakulteti përbehet nga<span> Departamenti i Elektronikës dhe Telekomunikacionit</span>, <span>Departamenti i Inxhinierisë Informatike </span>dhe <span>Departamenti i Bazave të Informatikës </span>(ish-QKZHTI Qendra për Kërkim dhe Zhvillim në Teknologjitë e Informacionit (ish-INIMA)). Departamentet  e Elektronikës dhe Telekomunikacionit dhe Inxhinierisë Informatike u krijuan nga ndarja e Departamentit të Elektronikës të Fakultetit të Inxhinierisë Elektrike, ndërsa Departamenti i Bazave të Informatikës  është vazhdim i Qendrës për Kërkim dhe Zhvillim (ish-INIMA i Akademisë së Shkencave).
