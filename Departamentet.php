</p>
<p>
<span>Departamenti i Bazave të Informatikës (DBI)</span> (ish-Qendra për Kërkim dhe Zhvillim) kryen veprimtari kërkimore zhvilluese të pavarur, si dhe në shërbim të procesit mësimor dhe studimeve pas universitare të FTI, në fushat e sistemeve të shfrytëzimit, sistemet e shpërndara, rrjetat kompjuterike, teknologjitë e Internetit, bazat e të dhënave dhe gjuhët e programimit. Departamenti është vazhdim i <span>Qendrës për Kërkim dhe Zhvillim </span> (ish-Institutit të Informatikës dhe Matematikës Aplikuar (INIMA).    
</p>
