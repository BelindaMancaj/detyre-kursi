</p>
<p>
<span>Departamenti i Elektronikës dhe Telekomunikacionit (DET)</span> është vazhdim i Departamentit të Elektronikës dhe përgatit inxhinierë në inxhinieri elektronike dhe inxhinieri e telekomunikacioneve. Departamenti ofron edukimin universitar dhe pas universitar, si dhe kërkim-zhvillim në fusha si sistemet elektronike, rrjetat wireless, rrjetat e kompjuterave të komandimit, studimi dhe matja e rrezatimit elektromagnetik të antenave të telefonisë celulare, projektimi i rrjetave të të dhënave dhe rrjetave mobile, cilësia e shërbimit në rrjetat, protokollet e rrjetave me fibra optike etj.  
</p>
