<p><span>Pyetje: Cilat janë kriteret për të hyrë në provim?</span></p>

 

<p><b>Përgjigje: Neni 20 - Parakushtet për hyrje në provim</b></p>

 

Studenti fiton të drejtën e hyrjes në provimin e një lënde, kur:<br>

1-ka plotësuar kriteret detyruese të vendosura për atë lëndë (detyrat, projektet)<br>
2-e lejon grafiku i ndërvarësisë së lëndëve<br>
3-plotëson kushtet që lidhen me frekuentimin e formave mësimore<br>
4-ka mbrojtur 100% detyrimet për praktikat mësimore, punët laboratorike, (në se lënda parashikon forma të tilla) , brenda afatit të parashikuar në grafikun e detyrimeve, para fillimit të sesionit të provimeve<br>
5-ka paguar tarifën e shkollimit
<p><span>Pyetje: Cili është minimumi i detyrimeve për kalimin në vitin pasardhës?</span></p>

 

<p><b>Përgjigje: Neni 24 - Kushtet e kalimit në vitin pasardhës</b></p>

 

1-Viti akademik mbyllet në përfundim të sesionit të vjeshtës.<br>
2-Për programet e studimit të ciklit të parë dhe të dytë, studenti i vitit të parë kalon në vitin e dytë nëse ai ka marrë jo më pak se 50% të krediteve (30 kredite) të vitit të parë.<br>
3-Për programet e studimit të ciklit të parë studenti i vitit të dytë kalon në vitin e tretë, nëse ai ka marrë jo më pak se 80 kredite, nga të cilat jo më pak se 50 kredite nga viti i parë.<br>
4-Për programet e integruara të studimit të ciklit të dytë, studenti i vitit të tretë kalon në vitin e katërt, nëse ai ka marrë jo më pak se 140 kredite nga të cilat jo më pak se 50 kredite nga viti i dytë.<br>
5-Studenti i vitit të katërt kalon në vitin e pestë, nëse ai ka marrë jo më pak se 200 kredite nga të cilat jo më pak se 50 kredite nga viti i tretë.<br>
6-Studenti që nuk plotëson kriteret për të kaluar në vitin pasardhës ka këto të drejta e detyrime:<br>
<dl>
<dd>a. I njihen të gjitha detyrimet e shlyera kur programet e lëndëve përkatëse janë të pranueshme për vitin në vazhdim</dd>
<dd>b. Mund të frekuentojë lëndët për të cilat është vlerësuar mbetës dhe është i detyruar të frekuentojë lëndët për të cilat ka detyrim frekuentimin</dd>
<dd>c. Duhet të shlyejë detyrimet e reja që lindin për shkak të ndryshimit të planit e programeve mësimore.</dd>
<dd>d. Duhet të paguajë tarifën e shkollimit.</dd>
 </dl>   
 <p><span>Pyetje: Çfarë duhet të bëj nëse dua të përmirësoj notën?</span></p>

 <p><b>Përgjigje: Neni 25 Përmirësimi i notave</b></p>
<dl>
<dt>1-Studenti ka të drejtë të kërkojë përmirësimin e notës. Një veprim i tillë lejohet kur nota që kërkon të përmirësojë studenti është e një detyrimi të vitit që ai sapo ka mbyllur. Studenti ka të drejtë të përmirësojë deri në dy nota në vit.</dt>
<dt>2-Për përmirësimin e notës, studenti provohet vetëm një herë dhe vetëm në sesionin e vjeshtës. Mbetet në fuqi nota me të cilën studenti vlerësohet në fund.</dt>
<dt>3-Kërkesa për përmirësim note i paraqitet dekanit jo më vonë se një javë pas përfundimit të sesionit të verës. Në bazë të vendimit që merr dekani, sekretaria mësimore kryen veprimet përkatëse.</dt>
<dt>4-Studenti mund të përmirësojë vetëm vlerësimin e marrë në provim, i cili i bashkëngjitet vlerësimit të marrë në elementët e tjerë të lëndës përgjatë vitit shkollor.</dt>
<dt>5-Në rastin e organizimit të moduleve të përbërë ku marrin pjesë disa disiplina, ku vlerësimi realizohet nëpërmjet provimeve të integruara, ose shlyrjes së modulit me pjesë, përmirësimi i notës bëhet për të gjithë modulin.</dt>
    </dl>
  <p><span>  Pyetje: Çfarë duhet të bëj nëse dua të ndërroj degë?</span></p>

 

<p><b>Përgjigje: Neni 26 - Transferimi i studimeve në të njëjtin program studimi</b></p>

 
<dl>
 <dt>1. Universiteti Politeknik pranon transferime studentësh në vite të ndërmjetme nga universitete të tjera, brenda ose jashtë vendit dhe lejon largimin e studentëve të tij për në universitetet të tjera, sipas procedurave të parashikuara në këtë Rregullore. Studenti duhet të ketë grumbulluar nga Matura Shtetërore sasi pikësh më të larta se pragu minimal i pikëve që është arritur në programin e studimit të kërkuar në vitin përkatës.Që studenti të ketë mundësi të transferojë studimet në Universitetin Politeknik, ai duhet të ketë fituar jo më pak se 30 kredite në programin përkatës të studimit. Në të kundërt, ai mund të fillojë studimet nga e para, duke ndjekur procedurat në hyrje të përcaktuar në kreun III të kësaj rregulloreje. Fakulteti mund t’i njohë studentit kreditet e fituara ose një pjesë të tyre në përputhje me procedurën e njohjes dhe ekuivalentimit të lëndëve.</dt>

 <dt>2. Procedurat për transferimin e studimeve si rregull fillojnë të paktën tre muaj para fillimit të vitit akademik dhe përfundojnë një javë para fillimit të vitit akademik.</dt>

 <dt>3. I interesuari paraqet një kërkesë në Degën Mësimore të UPT dhe dokumentacionin e nevojshëm, i cili përbëhet nga:</dt>

<dd>-Karta e studentit ose çdo dokument tjetër ku të vërtetohet që ka statusin e studentit në universitetin nga vjen</dd>
<dd>-Vërtetim në lidhje me programin e studimit dhe kursin ku ka qenë i regjistruar, vitin akademik që ka ndërprerë studimet</dd>
<dd>-Listën e detyrimeve të shlyera, kreditet dhe notat e fituara për çdo detyrim d. Planin mësimor dhe programet e lëndëve që ka shlyer</dd>
<dd>-Vërtetim për pikët e fituara në maturën shtetërore.</dd>
<dd>-Dokument identifikimi (çertifikatë e gjendjes civile me fotografi)</dd> 
<dt>4. Dega Mësimore ka përgjegjësi të verifikojë legjitimitetin e institucionit të arsimit të lartë nga vjen studenti, nivelin e programit të studimit që ka ndjekur studenti, origjinalitetin e dokumentave të paraqitur. Dega Mësimore ndjek gjithë praktikën me MASH për propozimin dhe miratimin e kuotave përkatëse. Me miratimin e kuotave, Dega Mësimore përcjell në fakultetet përkatëse kërkesat e grumbulluara, dokumentacionin e verifikuar si dhe udhëzimet e MASH-it.</dt>

<dt>5. Këshillat e fakulteteve mund të vendosin kritere specifike për pranimin e transferimit të studimeve, të cilat shpallen publikisht me fillimin e procedurës së transferimeve.</dt>

<dt>6. Dekani i fakultetit ngre komisionin “ad-hoc” të transferimeve me anëtarë të personelit akademik për të bërë përzgjedhjen e kandidatëve në përputhje me kriteret e vendosura dhe kuotave për çdo program studimi si edhe njohjen e plotë ose të pjesshme të krediteve të fituara prej tyre në programet apo vitet e mëparshme të studimit. Numri i anëtarëve të komisionit duhet të jetë 3-6 veta. Komisioni drejtohet nga dekani i fakultetit.</dt>

<dt>7. Komisioni bën ekuivalentimin e disiplinave që ka dhënë studenti me disiplinat respektive të programit të studimit, duke patur parasysh kreditet e lëndës, programin e lëndës dhe notën e marrë.</dt>

<dt>8. Në funksion të numrit të krediteve të njohura, dekani i fakultetit përcakton vitin ku do të regjistrohet studenti në përputhje me nenet e kësaj rregulloreje dhe përcakton kreditet debite që duhet të plotësojë studenti në përputhje me programin e studimit ku është pranuar.</dt>
</dl>
 

<p><b>Përgjigje: Neni 27 - Ndërrimi i programit të studimit</b></p>

 

Studenti mund të ndërrojë programin e studimit si brenda Universitetit Politeknik, ashtu edhe nga universitetet e tjera në Universitetin Politeknik. Për të ndërruar programin e studimit, studenti duhet:
<dl>
<dt>- të ketë marrë të gjitha kreditet e vitit të parë me një notë mesatare të ponderuar mbi 8</dt>
<dt>- të ketë grumbulluar nga Matura Shtetërore sasi pikësh më të larta se pragu minimal i pikëve që është arritur në programin e studimit të kërkuar në vitin përkatës</dt>
<dt>- të plotësojë kriteret specifike në hyrje nëse i kërkon programi përkatës i studimit</dt>
<dt>- Ndërrimi i programit të studimit bëhet me kërkesë të studentit. Procedurat që ndiqen janë në përputhje me nenin 26, pika 2,3,4 të kësaj rregulloreje.</dt>
<dt>- Vendimin për ndërrimin e programit të studimit e merr Rektori i UP, mbështetur në kuotat e viteve të ndërmjetme, të miratuara nga MASH, plotësimit të kritereve të shprehura në nenin 27, pika1 si dhe politikat e zhvillimit të Universitetit.</dt>
<dt>- Fakulteti përkatës bën njohjen e krediteve të fituara nga studenti për disiplina të ngjashme ose me objektiva formuese të njëjta. Për këtë veprohet në të njejtën mënyrë si në procesin e transferimit të studimeve, sipas nenit 26 të kësaj rregulloreje.</dt>
  </dl> 
 <p><span> Pyetje: Ç’ duhet bërë për të rifilluar studimet? Nëse largohem ka ndonjë procedurë?</span></p>

 

<p><b>Përgjigje: Neni 32 - Ndërprerja e studimeve, largimi</b></p>

 <dl>

<dt>1.Studenti mund të ndërpresë studimet për një periudhë kohore të caktuar. Në këtë rast ai duhet të bëjë një kërkesë pranë sekretarisë, në të cilën duhet të përcaktojë kohën e ndërprerjes së studimeve.</dt>

<dt>2.Me rifillimin e studimeve, studenti duhet të shlyejë detyrimet e reja që lindin për shkak të ndryshimit të planit dhe programeve mësimore të lëndëve.</dt>

<dt>3.Rifillimi i studimeve bëhet vetëm në fillim të vitit akademik dhe jo më vonë se dy javë nga fillimi i tij. Rifillimi ose ripranimi i studentit bëhet mbi bazën e një kërkese dhe dokumentacionit të nevojshëm të dorëzuar prej tij në sekretarinë e fakultetit.</dt>

<dt>4.Studenti mund të largohet nga universiteti, duke u çregjistruar përfundimisht. Për këtë ai bën një kërkesë me shkrim ku shpreh arsyet e largimit. Sekretaria mësimore, pranon kërkesën dhe ia paraqet dekanit të fakultetit për të marrë vendimin. Me marrjen e vendimit, sekretaria mësimore bën çregjistrimin e studentit, duke regjistruar në regjistrin themeltar datën e çregjistrimit dhe firmën e studentit. Studenti tërheq dokumentat e tij.</dt>

<dt>5.Çregjistrimi i studentit bëhet me vullnetin e tij të lirë (përveç rasteve të përjashtimit nga shkolla), në prani të studentit. Dokumentat origjinale i dorëzohen studentit.</dt>

 <dl>

<p><span>Pyetje: Mbrojtja e diplomës kryhet me tezë apo provim?</span></p>

 

<p><b>Përgjigje: Neni 34 - Modalitetet e diplomimit</b></p>

 
<dl>
<dt>1.Në përfundim të kursit të plotë të programit të studimit, studentët kanë të drejtë të diplomohen për të fituar titullin përkatës profesional të përcaktuar në statut. Diplomimi bëhet si rregull nëpërmjet: përgatitjes dhe mbrojtjes së një teze diplome. Në raste të veçanta, vetëm për programet e studimit të ciklit të parë, diplomimi mund të bëhet me provim formimi në përputhje me kurrikulat mësimore të programeve të studimit.</dt>
<dt>2.Studenti nuk mund të mbrojë temën e diplomës / të japë provimin e formimit pa shlyer më parë të gjitha detyrimet dhe pa fituar numrin e krediteve të parashikuara në planet mësimore të programit përkatës të studimit.</dt>
<dt>3.Studenti, para mbrojtjes së punimit të diplomës/dhënies së provimit të formimit, duhet të ketë likuiduar të gjitha detyrimet financiare dhe materiale që mund të ketë me universitetin, bibliotekën shkencore, bibliotekën kombëtare, konviktet, etj.</dt>
<dt>4.Fakultetet përkatëse, brenda një muaji nga dita e fillimit të vitit shkollor, u bëjnë të njohur studentëve të vitit të fundit programet e provimeve të formimit dhe/ose tezat e diplomës. </dt>
  </dl>
  <p><span>Pyetje: Si përgatitet teza e diplomës nivelit të parë?</span></p>

 

<p><b>Përgjigje: Neni 35 - Teza e diplomës të nivelit të parë</b></p>

 
<dl>
<dt>1.Teza e diplomës të nivelit të parë është një punim, në formën e një projekti apo përmbledhjeje punimesh të kryera në fushat e programit përkatës të studimit. Teza e diplomës përmban kërkesa, mënyra e shtrimit të të cilave i jep mundësi studentit, që nëpërmjet njohurive të fituara gjatë periudhës së shkollimit, të tregojë aftësitë e tij për të zgjidhur elementë të veçantë të detyrave projektuese, probleme të prodhimit apo për të punuar me literaturën në fushën përkatëse.</dt>
<dt>2.Teza e diplomës është e lidhur me disiplina të përafërta dhe mund të jetë individuale ose kolektive (dy-tre studentë). Në rastet e punimeve kolektive, studentet kanë të ndarë çështjet që do të trajtojnë, duke respektuar një ndarje afërsisht të barabartë të volumit të punës që do të kryejnë.</dt>
<dt>3.Tezat e diplomës dhe specialistët që do të udhëheqin punimin e tyre, përcaktohen nga departamenti përkatës dhe miratohen nga dekani i fakultetit. Në përcaktimin e tezave të diplomës, departamentet dhe fakultetet bashkëpunojnë me institutet kërkimore shkencore të UPT si dhe me institucione të tjera publike apo private që punojnë në fushat përkatëse.</dt>
<dt>4.Studenti ka të drejtë të shprehë dëshirën e tij në lidhje me tezën e diplomës që kërkon të zhvillojë, dhe udhëheqësin e tij. Ai mund të propozojë edhe vetë një tezë tjetër diplome sëbashku me udhëheqësin përkatës. Departamenti shqyrton të gjitha kërkesat specifike të studentëve, duke i harmonizuar ato me kushtet aktuale.</dt>
 </dl>

 <p><span>Pyetje: Si përgatitet teza e diplomës së nivelit të dytë dhe e diplomave të integruara të nivelit të dytë?</span></p>

 

 <p><b>Përgjigje: Neni 36 - Teza e diplomës së nivelit të dytë dhe e diplomave të integruara të nivelit të dytë</b></p>
<dl>
<dt>1.Teza e diplomës së nivelit të dytë dhe e diplomave të integruara të nivelit të dytë është një punë e pavarur dhe krijuese e studentit në formën e një projekti apo studimi me karakter përgjithësues në fushën e programit të studimit që ka kryer. Teza e diplomës përmban një sërë detyrash e kërkesash, mënyra e shtrimit e të cilave i jep mundësi studentit, që nëpërmjet njohurive të fituara gjatë periudhës së shkollimit dhe punës konkrete studimore e projektuese, !--[if !mso]të tregojë aftësitë e tij profesionale dhe kompetencën për të kryer studime e projektime në fushat përkatëse.</dt>
<dt>2.Teza e diplomës është e lidhur me disa disiplina të përafërta dhe mund të jetë individuale ose kolektive (dy-tre studentë), në rastet e tezave studimore komplekse. Në rastet e punimeve kolektive, studentët kanë të ndarë çështjet që do të trajtojnë, duke respektuar një ndarje afërsisht të barabartë të volumit të punës që do të kryejnë.</dt>
<dt>3.Tezat e diplomës dhe caktimi i specialistëve që do të udhëheqin punën e tezave të diplomës, përcaktohen nga departamenti përkatës dhe miratohen nga dekani i fakultetit. Në përcaktimin e tezave të diplomës, departamentet dhe fakultetet bashkëpunojnë me Institutet kërkimore shkencore të UPT si dhe me institucione të tjera publike apo private që punojnë në fushat përkatëse.</dt>
<dt>4.Studenti ka të drejtë të shprehë dëshirën e tij në lidhje me tezën e diplomës që kërkon të zhvillojë, udhëheqësin dhe konsulentin e tij. Ai mund të propozojë edhe vetë një tezë tjetër diplome së bashku me udhëheqësin përkatës. Departamenti shqyrton të gjitha kërkesat specifike të studentëve, duke i harmonizuar ato me kushtet aktuale, pa cënuar nivelin e cilësinë e punimeve të diplomave.</dt>
 </dt>

 <p><span>Pyetje: Si përgatitet punimi i diplomës së nivelit të parë?</span></p>

 

 <p><b>Përgjigje: Neni 37 - Përgatitja e punimit të diplomës së nivelit të parë</b></p>

 <dl>
<dt>1.Udhëheqësi i temës së diplomës përgatit programin e punës së studentit, duke vendosur edhe afatet e përafërta të realizimit të tij. Studenti duhet të respektojë afatet e vendosura për të bërë të mundur mbrojtjen me sukses të temës në sesionin normal të mbrojtjes së diplomës.</dt>
<dt>2.Udhëheqësi përcakton grafikun e konsultimeve të programuara me studentin, përgjatë të cilave konsultohet dhe kontrollohet puna e bërë sipas programit të miratuar. Grafiku i konsultimeve është i detyrueshëm të zbatohet nga studenti.</dt>
<dt>3.Departamenti organizon dy kontrolle të ndërmjetëm, nëpërmjet të cilëve kontrollohet volumi i punës së kryer dhe cilësia e saj. Përgjatë këtyre kontrolleve mund të rekomandohen edhe ndryshime apo përmirësime të programit të temës, të cilat nuk përmbysin thelbin e punimit. Studenti dhe udhëheqësi duhet t’i marrin parasysh rekomandimet e komisionit të kontrollit.</dt>
<dt>4.Studenti fiton të drejtën për të mbrojtur diplomën, kur ai ka përfunduar të gjithë programin e punës dhe ka kryer kontrollin në departament. Para mbrojtjes së tezës së diplomës, udhëheqësi rishikon dhe miraton punimin e kryer nga studenti dhe lejon mbrojtjen e punimit duke firmosur tezën e diplomës. Përgjegjësi i departamentit plotëson fletën e kontrollit të tezës së diplomës, në të cilën regjistrohet kontrolli i kryer dhe vlerësimi me fjalë për të.</dt>
 </dl>

 <p><span>Pyetje: Si përgatitet punimi i diplomës së nivelit të dytë dhe e diplomave të integruara të nivelit të dytë?</span></p>

 

 <p><b>Përgjigje: Neni 38 - Përgatitja e punimit të diplomës së nivelit të dytë dhe e diplomave të integruara të nivelit të dytë</b></p>

 
<dl>
<dt>1.Udhëheqësi i temës së diplomës përgatit programin e punës së diplomantit, duke vendosur edhe afatet e përafërta të përfundimit të çdo pike të programit. Studenti duhet të respektojë afatet e vendosura për të bërë të mundur mbrojtjen me sukses të temës në sesionin normal të mbrojtjes së diplomës.</dt>
<dt>2.Udhëheqësi përcakton grafikun e konsultimeve të programuara me studentin, përgjatë të cilit konsultohet dhe kontrollohet puna e bërë sipas programit të miratuar. Grafiku i konsultimeve është i detyrueshëm të zbatohet nga studenti.</dt>
<dt>3.Departamenti organizon tre kontrolle periodike, në të cilat kontrollohet volumi i punës së kryer dhe cilësia e saj. Përgjatë këtyre kontrolleve mund të rekomandohen edhe ndryshime apo përmirësime të programit të tezës, të cilat nuk përmbysin thelbin e punimit. Studenti dhe udhëheqësi duhet t’i marrin parasysh rekomandimet e komisionit të kontrollit.</dt>
<dt>4.Studenti fiton të drejtën për të mbrojtur diplomën, kur ai ka përfunduar të gjithë programin e punës dhe ka kryer të gjitha kontrollet në departament. Para mbrojtjes së tezës së diplomës, udhëheqësi rishikon dhe miraton punimin e kryer nga studenti. Ai jep mendimin e tij me shkrim, ku deklarohet realizimi i programit të punës të tezës së diplomës, niveli cilësor, elementët e punës krijuese të studentit dhe lejimi i mbrojtjes së punimit.Përgjegjësi i departamentit plotëson fletën e kontrollit të tezës së diplomës, në të cilën regjistrohen kontrollet e kryera dhe vlerësimet me fjalë për secilën prej tyre.</dt>
</dl>

<p><span>Pyetje: Kur dhe si kryhet mbrojtja e diplomës?</span></p>

 

<p><b>Përgjigje: Neni 39 - Organizimi i mbrojtjes së diplomës</b></p>

 
<dl>
<dt>1.Universiteti Politeknik organizon tri sesione për mbrojtjen e diplomave: në verë, vjeshtë dhe dimër. Sesionet shpallen nga Rektori i UP. Datat e zhvillimit të mbrojtjes sipas sesioneve përkatëse, përcaktohen nga dekani. </dt>
<dt>2.Përgatitja dhe paraqitja e tezës së diplomës bëhet sipas normave të përcaktuara nga kjo rregullore dhe rregullave të tjera të vendosura nga fakulteti dhe ndjek procedurat e miratuara nga Këshilli i Fakultetit përpara se ajo të mbrohet në komisionin përkatës. Të gjitha tezat e diplomave, para se të lejohen për mbrojtje, miratohen nga dekani i fakultetit.</dt>
<dt>3.Punimi i diplomës mbrohet para një komisioni të posaçëm, të përbërë nga staf akademik i brendshëm dhe i ftuar, i propozuar nga departamentet përkatëse dhe i miratuar nga dekani. Komisioni i diplomës respekton në çdo rast, për secilin diplomant procedurën dhe rregullat përkatëse të vendosura nga Këshilli i Fakultetit. Komisioni i mbrojtjes së diplomave funksionon kur janë jo më pak se 2/3 e anëtarëve të tij. Mbrojtja e diplomës është publike.  </dt>
<dt>4.Për programet e studimit të ciklit të parë, numri i anëtarëve të komisionit të diplomave është 3-5, ndërsa për programet e studimit të ciklit të dytë, numri i anëtarëve të komisionit është 5-7. </dt>
 </dl>

 <p><span>Pyetje: Çfarë mundësish ka nëse teza vlerësohet me notë negative?</span></p>

 

 <p><b>Përgjigje: Neni 40 - Vlerësimi i punimit.</b></p>

 
<dl>
<dt>1.Në rast se studenti vlerësohet me notë negative në mbrojtjen që i bën tezës së tij, ai mund të rimbrojë të njejtën tezë ose të mbrojë një tezë të re brenda një periudhe jo më vonë se tre vjet.</dt>
<dt>2.Gjatë periudhës tre vjeçare nga koha e përfundimit të studimeve të plota, diplomanti ka të drejtë të mbrojë tezën e tij deri në dy herë.</dt>
<dt>3.Nëse në të dy rastet diplomanti është vlerësuar nga komisioni me notë negative ose ai nuk paraqitet për të bërë mbrojtjen e punimit brenda afateve maksimale (tre vjeçare), atëhere studenti humb të drejtën e diplomimit. </dt> 
 </dl>  
