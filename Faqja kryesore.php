    <p>Fakulteti i Teknologjisë së Informacionit (FTI) është një nga njësitë kryesore të Universitetit Politeknik
     të Tiranës (UPT). UPT është Institucion Publik i Arsimit të Lartë, me qendër në Tiranë, i cili shtrin aktivitetin 
     e tij mësimor dhe shkencor brenda kufijve të Republikës së Shqipërisë, ofron studime me kohë të plotë në tre cikle
      dhe synon të përgatisë specialistë dhe shkencëtarë në fushat inxhinierike.
    Fakulteti i Teknologjisë së Informacionit, pjesë e Universitetit Politeknik të Tiranës, është e para shkollë e 
    lartë në Shqipëri për përgatitjen e specialistëve të lartë në fushat e inxhinierisë elektronike, inxhinierisë së
     telekomunikacionit dhe inxhinierisë informatike. Përgatitja e specialistëve në këto fusha ka për qëllim punësimin
      e tyre si inxhinierë në projektimin, realizimin dhe mirëmbajtjen e sistemeve të  përpunimit të informacionit në 
      fushën e Teknologjisë së Informacionit (TI). Të diplomuarit nga FTI janë të aftë të punojnë në funksione me profil
      të lartë në nivel kombëtar dhe ndërkombëtar.Institucionet shkencore të vendit gjejnë në FTI partner serioz dhe të
       përkushtuar për përgatitjen e projekteve dhe veprave të tjera shkencore në dobi të vendit. Bizneset në fushën e 
       Telekomunikacionit dhe të Teknologjisë së Informacionit duke bashkëpunuar me FTI kanë mundësi të aplikojnë 
       arritjet më të fundit të shkencës dhe teknikës në bizneset e tyre duke rritur prodhimin dhe rendimentin e punës
        dhe ulur koston.
    </p>
