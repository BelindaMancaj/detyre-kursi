    <h3>RREGULLORE E SHKOLLËS SË DOKTORATËS</h3>
    <div class="fund">
<p><b>Rregullorja e Shkollës së Doktoratës</b></p>
<p>Përgatitur nga stafi akademik i FTI, miratuar nga Këshilli i Profesorëve, </p>
<p>Fakulteti i Teknologjisë së Informacionit </p>
<p>Universiteti Politeknik i Tiranës.</p>
</div>
 

<p><b>Art. 1 – Objektivat e Shkollës së Doktoratës</b></p>
<dl>
<dt>1. Shkolla e Doktoratës përfaqëson edukimin e nivelit të tretë ofruar pranë Universitetit Politeknik të Tiranës.</dt>
<dt>2. Kualifikimi i studentëve të Doktoratës ka për qëllim pajisjen e studentëve me njohuritë dhe aftësitë e nevojshme për të kryer kërkim shkencor me nivel të lartë pranë universiteteve, trupave publike, dhe organizatave private; duke zhvilluar më tej progresin shkencor dhe teknologjik si dhe krijimin e një klase udhëheqëse profesionistësh me nivel të lartë njohurish, të aftë të bashkëpunojnë në nivele ndërkombëtare.</dt>
<dt>3. Këto objektiva realizohen përmes aktiviteteve mësimore me cilësi të lartë.</dt>
 </dl>

 <p><b>Art. 2 – Kërkesat Minimale</b></p>
<dl>
<dt>Komisioni i Vlerësimit, Senati i Universitetit dhe Komiteti i Kërkimit Shkencor për të vendosur krijimin apo vazhdimin e Shkollës së Doktoratës së bashku apo edhe në mënyrë të pavarur duhet të verifikojnë ekzistencën e kërkesave minimale të nevojshme, të përcaktuara si më poshtë:</dt>

<dd>a) ekzistenca e burimeve financiare të nevojshme dhe strukturave shkencore specifike për të zhviluar Doktoratën Kërkimore, studimet e studentëve dhe aktiviteteve kërkimore;</dd>

<dd>b) mundësinë për të realizuar marrëveshje partneriteti me organizata private apo publike të huaja edhe shqiptare që mund t’i ofrojë studentëve eksperiencë pune të një niveli të lartë;</dd>

<dd>c) implementimin e një sistemi vlerësimi lidhur me vazhdueshmërinë e kërkesave të parashtruara më sipër dhe aftësia për të gjykuar nëse Doktorata e ofruar kënaq objektivat e vendosur lidhur me perspektivën profesionale dhe nivelin e trajnimit të studentëve.</dd>
</dl>
 

<p><b>Art.3 – Përbërja e Shkollës së Doktoratës</b></p>
<dl>
<dt>1. Shkolla e Doktoratës trajton fusha shkencore që i referohen përmbajtjes së një displine shkencore apo edhe një shkrirjeje të disa disiplinave.</dt>
<dt>2. Çdo Shkollë Doktorate mund të ndahet në disa programe Doktoratash. Komiteti i Shkollës së Doktoratës  (Këshilli i Profesorëve) zgjedh fushat, ashtu si përcaktohet në Artikullin 6 të paraqitur më poshtë.</dt>
</dl>
<p><b>Art. 4 – Marrëveshjet me universitete të tjera shqiptare dhe të huaja</b></p>
<dl>
<dt>1. Universiteti Politeknik i Tiranës, me qëllim promovimin e marrëdhënieve mes shkollës së Doktoratës pranë këtij universiteti dhe një universiteti tjetër të huaj apo shqiptar, përkrah dhe promovon firmosjen e marrëveshjeve që drejtohen nga:</dt>
<dd>- Njohja reciproke e aktiviteteve shkencore dhe mësimore të zhvilluara në  të dy Shkollat e Doktoratës;</dd>

<dd>- Përftimi i një titulli të dyfishtë, i lëshuar individualisht nga universitet partnere.</dd>

<dt>2. Këto marrëveshje, kur rregullojnë marrëveshje me universitete partnere ndërkomëtare  mund të rregullojnë gjithashtu edhe formimin e Komiteteve të Pranimit për pranimin në Shkollën e Doktoratës dhe takimet për formimin e Komitetit të Provimeve.</dt>
<dt>3. Kur Universiteti Politeknik i Tiranës krijon marrëveshje me universitete të huaja, shkolla e Doktoratës mund të përcaktohet si ndërkombëtare nëse:</dt>
<dt>4. Ka një numer të konsiderueshëm profesorësh të huaj në Komitetin e Shkollës së Doktoratës;</dt>
<dt>5. Për lëndët ofrohet një gjuhë e dytë zyrtare: anglisht ose gjuha e universitetit partner;</dt>
<dt>6. Në website-in e Universitetit paraqitet versioni anglisht i lajmërimit për hapjen e konkursit për t’u regjistruar në Shkollën e Doktoratës;</dt>
<dt>7. Komiteti i shkollës të përbëhet nga anëtarë  që i përkasin institucioneve të njohura të huaja;</dt>
<dt>8. Ka një numër të konsiderueshëm studentësh të huaj.</dt>
 </dl>

 <p><b>Art. 5 - Trupi drejtues i Shkollës së Doktoratës</b></p>
<dl>
<dt>1. Sipas rregullores në fjalë trupi drejtues i Shkollës së Doktoraturës përbëhet nga: Drejtuesi i Shkollës (Koordinatori i Programit të Studimit), Komiteti i Shkollës së Doktoratës (Këshilli i Profesorëve).</dt>
<dt>2. Drejtuesi i Shkollës dhe antarët e tjerë të Komitetit të Shkollës së Doktoratës nuk mund të kryejnë të njëjtin funksion në një tjetër Shkollë Doktorate Kërkimi  në Shqipëri.</dt>
</dl>
<p><b>Art. 6 - Komiteti i Shkollës së Doktoratës</b></p>
<dl>
<dt>1. Komiteti i Shkollës së Doktoratës përbëhet nga profesor ose profesor të asociuar dhe kërkues shkencor; të paktën pesë anëtar të Komitetit duhet të jenë nga Universiteti Politeknik i Tiranës; të paktën një profesor duhet të përfaqësojë një disiplinë të Doktoratës; si dhe përfaqësues të administrates së fakultetit. Studentët përfaqësues mund të marrin pjesë në takimet e Komitetit të Shkollës së Doktoratës, por vetëm për çështje që kanë të bëjnë me përparimin e përgjithshëm të Shkollës; dy përfaqësues të tillë zgjidhen çdo vit mes studentëve të regjistruar pranë Shkollës së Doktoratës.</dt>
<dt>2. Komiteti i Shkollës së Doktoratës formohet nga anëtarë të Këshillit të Profesorëve dhe kërkues shkencorë (me gradën “Doktor”) të çdo disipline të Shkollës së Doktoratës. Komiteti mund të përfshijë gjithashtu ekspertë të huaj, të cilët nuk duhet të jenë më shumë së gjysma e anëtarëve të përcaktuar në paragrafin e mësipërm.</dt>
<dt>3. Profesorët dhe kërkuesit mund të jenë antarë të Komiteteve të Shkollave të Doktoratës në çdo Universitet tjetër Shqiptar.</dt>
<dt>4. Menjëherë pas fillimit të Shkollës së Doktoratës, mbledhja e parë duhet të thirret nga Përgjegjësi i Departamentit ose Qendrës Kërkimore ku atashohet Shkolla e Doktoratës, jo më vonë së tridhjetë ditë pas fillimit të saj.</dt>
<dt>5. Komiteti i Shkollës së Doktoratës:</dt>
<dt>6. organizon lëndët dhe aktivitetet e tjera të Shkollës së Doktoratës dhe përcakton programet;</dt>
<dt>7. aprovon pjesëmarrjen e anëtarëve të rinj pranë Komitetit të Shkollës së Doktoratës;</dt>
<dt>8. organizon çdo vit zhvillimin e programit teorik të moduleve, përfshirë aktivitetet dhe kalendarin akadamik;</dt>
<dt>9. kontrollon rezultatet kërkimore të çdo studenti që të sigurohet se secili student ka përvetësuar metodologjinë e kërkimit shkencor.</dt>
 </dl>

 <p><b>Art. 7 - Proçedura e pjesëmarrjes në Shkolla Doktorate/Doktorata Kërkimore me universitete të tjera</b></p>
<dl>
<dt>1. Pas konsultimit me Fakultetet e përfshira në marrëveshje, Fakulteti mund të paraqesë tek Rektori i Universitetit një propozim lidhur me pjesëmarrjen në Shkollën e Doktoratës/Doktoratën Kërkimore.</dt>
<dt>2. Propozimi duhet të përfshij informacion lidhur me:</dt>
<dt>3. Fushën e kërkimit dhe disiplinën përkatëse të Doktoratës;</dt>
<dt>4. Temat specifike të kërkimit për ciklin e propozuar;</dt>
<dt>5. Kohëzgjatjen, qëllimet dhe metodat për implementimin e Doktoratës;</dt>
<dt>6. Universitetin, trupat publike apo organizatat private të përfshira;</dt>
<dt>7. Çdo detyrim financiar që fakulteti duhet të shlyejë për këtë pjesëmarrje;</dt>
<dt>7. Përbërjen e Komitetit të Shkollës së Doktoratës;</dt>
<dt>8. Çdo detyrim financiar që duhet të plotësojë Univeristeti Politeknik i Tiranës.</dt>
<dt>9. Në një mbledhje të Senatit të Universitetit, Rektori vendos për çdo vit akademik me anë të një dekreti Shkollat e Doktoratës/Doktoratat e Kërkimit me të cilat Universiteti do të atashohet.</dt>
 </dl>  
 <p><b>Art. 8 - Lajmërim për hapjen e konkursit</b></p>
<dl>
<dt>1. Çdo lajmërim për hapjen e konkursit për pranimin në Shkollën e Doktoratës, shpallur me dekret të Rektorit, duhet publikuar në gazetën zyrtare të Republikës së Shqipërisë dhe të njoftohet me mjetet e shtypit dhe faqen web të Universitetit. Lajmërimi duhet të përfshijë:</dt>
<dt>2. Kurrikulën mësimore të Shkollës së Doktoratës;</dt>
<dt>3. Kohëzgjatjen e Shkollës së Doktoratës;</dt>
<dt>4. Afatin për paraqitjen e aplikimeve;</dt>
<dt>5. Mënyrën e përdorur për të kryer përzgjedhjen;</dt>
<dt>6. Kriteret e vlerësimit të kandidatëve;</dt>
<dt>7. Numrin maksimal të kandidatëve që do të pranohen;</dt>
<dt>8. Numrin e bursave të ofruara;</dt>
<dt>9. Afatin e regjistrimit për kandidatët fitues në Shkollën e Doktoratës;</dt>
<dt>10. Kriteret për pranimin në vitin pasues pas vitit të parë të studimeve;</dt>
<dt>11. Aktet që mund të sjellin pezullimin dhe  revokimin e bursave;</dt>
<dt>12. Taksat që duhen paguar nga studentët dhe rregullin për specifikimin e rasteve të veçanta ku ndodhin përjashtime nga rregullat e përgjithshme;</dt>
<dt>13. Të drejtat dhe detyrimet e çdo studenti.</dt>
<dt>14. Lajmërimi për pranimet mund të përfshijë gjithashtu mundësinë që studentët rezidentë në vende të tjera të kryejnë proçedurën e pranimit në univeristetet e vendeve të tyre, në rastet e Shkollës së Doktoratës ndërkombëtare.</dt>
</dl> 

<p><b>Art. 9 - Kërkesat për pranimin në Shkollën e Doktoraurës</b></p>

<p>Aplikimet për Doktoratë pranohen nga kandidatë që zotërojnë një titull studimi “Master i nivelit të dytë”, “Diplomë e nivelit të dytë”,  “Diplomë e Integruar e nivelit të dytë”, “Diplomë e sistemit 5-vjeçar”, ose një titull studimi ekuivalent fituar jashtë shtetit dhe të konvaliduar dhe të njohur zyrtarisht nga organet kompetente, sipas akteve ligjore e nënligjore në fuqi.</p>

 

<p><b>Art. 10 - Komiteti i Pranimit në Shkollën e Doktoraturës</b></p>
<dl>
<dt>1. Këshilli i Profesorëve cakton një Komitet Pranimi (Komision Ad-Hoc) për çdo Program studimi të Shkollës së Doktoratës.</dt>
<dt>2. Komiteti i Pranimit përbëhet të paktën nga tre antarë të Këshillit të Profesorëve, si dhe kërkues shkencorë (me gradën “Doktor”) që janë ekspertë në disiplinat e Shkollës së Doktoratës.</dt>
<dt>3. Komiteti i Pranimit duhet të drejtohet nga profesori më me eksperience; në rastin e kushteve të barabarta përsa i përket aftësive të profesorëve zgjidhet si drejtues profesori më i vjetër në moshë.</dt>
</dl>

<p><b>Art. 11 - Pranimet</b></p>
<dl>
<dt>1. Pranimet në Shkollën e Doktoratës do të përcaktohen bazuar në një klasifikim të pergjithshëm sipas meritave, kryer nga Komiteti i Pranimit, derisa të plotësohet numri i vendeve të përcaktuara në lajmërimin e publikuar nga Rektori.</dt>
<dt>2. Kandidatët e pranuar në Doktoratë bazuar në listën finale do të humbin të drejtën e tyre për t’u regjistruar në Shkollë nëse nuk paraqesin dokumentat e nevojshme për regjistrim brenda afateve të përcaktuara nga lajmërimi.</dt>
<dt>3. Renditja finale e kandidatëve duhet publike në website-in e Shkollës.</dt>
<dt>4. Nëse një prej fituesve tërhiqet para fillimit të Doktoratës, vendi do t’i afrohet aplikuesit të parë në renditjen e listës.</dt>
 </dl>

 <p><b>Art. 12 - Aplikime të Pavlefshme</b></p>

<p>Studentët e regjistruar në një program tjetër studimi, master, ose Doktoratë, nuk mund të regjistrohen njëkohësisht në një Shkollë Doktorate. </p>
<p><b>Art. 13 - Të drejtat dhe detyrimet e studentit të Doktoratës</b></p>
<dl>
<dt>1. Studentët e regijstruar në Shkollën e Doktoratës duhet të ndjekin leksionet, seminaret dhe të gjitha aktivitetet mësimore, të kryejnë aktivitetet e kërkimit shkencor duke punuar me kohë të plotë në strukturat e përcaktuara për këtë qëllim dhe të dorëzojnë në fund të vitit një raport mbi punën e kryer pranë Komitetit të Shkollës së Doktoratës.</dt>
<dt>2. Komiteti i Shkollës së Doktoratës mund të vendosë të përjashtojë një student nga Shkolla e Doktoratës nëse verifikohet një nga rastet e mëposhtme:</dt>
<dt>3. Shprehja e një vlerësimi negativ nga ana e Komitetit të Shkollës së Doktoratës lidhur me pranimin në vitin tjetër të studimeve; në këtë rast Komiteti i Shkollës së Doktoratës verifikon realizimin e rezultateve të kërkuara si edhe përkushtimin dhe zellin e treguar në punën kërkimore të kryer;</dt>
<dt>4. mungesa të gjata dhe të pajustifikuara.</dt>
<dt>5. Studenti i Doktoratës ka të drejtë të marrë leje-lindje,  leje për të kryer shërbimin ushtarak ose për problem jetike familjare dhe sëmundje serioze.</dt>
<dt>6. Mungesa për arsye të tjera të pa listuara më sipër duhet të autorizohet nga Komiteti i Shkollës së Doktoratës.</dt>
</dl> 

<p><b>Art. 14 - Dizertacioni i Doktoratës</b></p>
<dl>
<dt>1. Grada “Doktor i Shkencave” fitohet kur studenti kalon me sukses provën finale të mbrojtjes së Dizertacionit të Doktoratës, që mund të përsëritet vetëm një herë.</dt>
<dt>2. Në fund të studimeve të Shkollës së Doktoratës, studenti i regjistruar në vitin e fundit dorëzon kërkesën për t’u pranuar në provën finale pranë Komitetit të Doktoratës.</dt>
<dt>3. Komiteti i Shkollës së Doktoratës kërkon një rekomandim nga udhëheqësi përsa i përket dizertacionit të studentit dhe vendos nëse kandidati do të pranohet në provën finale, duke hartuar një raport të detajuar lidhur me punën e kandidatit gjatë Doktoratës.</dt>
<dt>4. Kandidati mund të kryejë provën finale pasi Komiteti i Shkollës së Doktoratës vlerëson pozitivisht dizertacionin.</dt>
<dt>5. Për arsye të justifikuara që paraprijnë dorëzimin e dizertacionit në afatin e caktuar, Rektori, me propozim të Këshillit të Profesorëve, mund të akordojë një shtyrje të afatit për dorëzimin e dizertacionit në vitin akademik pauses. Një shtyrje e tillë mund të akordohet deri në dy herë.</dt>
<dt>6. Për të përftuar një shtyrje të afatit një kandidat duhet të dorëzojë një kërkesë dhe vlerësim pozitiv të Komitetit të Shkollës së Doktoratës.</dt>
 </dl>

<p><b>Art. 15 – Juria e vlerësimit për fitimin e gradës “Doktor”</b></p>
<dl>
<dt>> Jo më vonë se dy muaj, Këshilli i Profesorëve duhet të propozojë anëtarët e jurisë për provën finale (tezën e doktoratës), sipas akteve ligjore e nënligjore në fuqi.</dt>
<dt>> Juria miratohet nga Rektori, bazuar në propozimin e Këshillit të Profesorëve, jo më vonë se nëntëdhjetë ditë pas përfundimit të Shkollës së Doktoratës.</dt>
 </dl>

<p><b>Art. 16 – Mbrojtja e tezës së Doktoratës dhe fitimi i gradës “Doktor”</b></p>
<dl>
<dt>1. Teza e doktoratës do të paraqitet përpara Jurisë dhe konsiston në mbrojtjen publike të dizertacionit. Grada “Doktor” fitohet vetëm pas kalimit me sukses të mbrojtjes së tezës së Doktoratës.</dt>
<dt>2. Kandidati dhe antarët e Jurisë duhet të njoftohen të paktën shtatë ditë përpara ditës së mbrojtjes, lidhur me vendin dhe orarin e mbrojtjes me anë të një njoftimi zyrtar.</dt>
<dt>3. Kandidati duhet të pajisë çdo anëtar të Jurisë me një kopje të dizertacionit; kjo e fundit shoqërohet nga raporti i përpiluar nga Komiteti i Shkollës së Doktoratës dhe prezantimi i përgatitur nga udhëheqësi. Kandidati duhet të dorëzojë tre kopje të dizertacionit final pranë administratës së fakultetit, e cila është përgjegjëse për depozitimin e tyre pranë Bibliotekës Kombëtare dhe Bibliotekës  së Universitetit.</dt>
<dt>4. Juria duhet të formulojë një raport për secilin kandidat. Raporti përbën një pjesë esenciale të vlerësimit përfundimtar për Tezën e Doktoratës.</dt>
<dt>5. Marrëveshjet e bashkëpunimit ndërkombëtar mund të sjellin përfshirjen e rregullave shtesë për lëshimin e gradës “Doktor”.</dt>
</dl> 

<p><b>Art. 17 – Hyrja në fuqi</b></p>

<p>Rregullorja në fjalë hyn në fuqi duke filluar nga 1 Tetor 2009. </p>  
